class Solution{
    public:
        int modulo(string s,int m)
        {
            //code here
            int res=0;
            int num=1;
            reverse(s.begin(),s.end());
            for(auto i:s){
                if(i=='1'){
                    res=(res+num)%m;
                }
                num=(num*2)%m;
            }
            return res;
        }
};

